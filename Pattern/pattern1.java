package Practicals;

public class pattern1 {
	public static void main(String[] args) {
		int count=4;
		for( int i=0;i<4;i++) {	
			for(int j=1;j<=count;j++) {
				System.out.print(j); }
			for(int r=0;r<(2*i);r++) {
				System.out.print("*");
			}
				for(int k=count;k>=1;k--) {
					System.out.print(k);
				}System.out.println();
				count--;
				}
				} } 

				

					
				
	
			
		
			
				
			
		
		
	


